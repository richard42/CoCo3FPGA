		begin
			PIXEL_COUNT <= 10'd014;
			HBORDER <= 1'b1;								//Turn on border for 640 mode
		end
		10'd015:				// Turn off horizontal blanking so first character can be displayed
		begin
			HBLANKING <= 1'b0;							// Turn off blanking
			HBORDER <= 1'b1;
			HSYNC <= 1'b1;									// Not H Sync
			PIXEL_COUNT  <= 10'd016;					// Next step
		end
		10'd527:												// 512 + 16 -1
