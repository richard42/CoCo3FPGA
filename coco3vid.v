			({COCO,BP,CRES[0],CHARACTER3[7],CHAR_LATCH_0[10:8]}==7'b0010001)	?	PALETTE1:
			({COCO,BP,CRES[0],CHARACTER3[7],CHAR_LATCH_0[10:8]}==7'b0010010)	?	PALETTE2:
