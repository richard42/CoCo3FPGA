/*****************************************************************************
* Keeps track of the real line number, and controls VSYNC and VBlanking.
* Does not keep track of the lines per row
*
*	LPR				Lines
*
*	00 or COCO =1	192
*	01					200
*	10					210
*	11					225	(25*9)
******************************************************************************/
always @ (negedge HSYNC or negedge RESET_N)
begin
	if(~RESET_N)
	begin
		LINE <= 10'd00;
		VBLANKING <= 1'b0;
		VSYNC <= 1'b1;
	end
	else
	case (LINE)
// Video
	10'd383:								// End of 192 line display
	begin
		LINE <= 10'd384;
		if((LPF == 2'b00) || (COCO == 1'b1))		// Standard COCO modes are always 192
		begin
			VBLANKING <= 1'b1;
		end
	end
	10'd399:								// End of 200 line display
	begin
		LINE <= 10'd400;
		if(LPF == 2'b01)
		begin
			VBLANKING <= 1'b1;
		end
	end
	10'd419:								// End of 210 line display
	begin
		LINE <= 10'd420;
		if(LPF == 2'b10)
		begin
			VBLANKING <= 1'b1;
		end
	end
	10'd449:								// End of 225 line display
	begin
		LINE <= 10'd450;
		VBLANKING <= 1'b1;
	end
//End of Border, start of porch
	10'd467:
	begin
		VBORDER <= 1'b0;
		LINE <= 10'd468;
	end
// End of Porch, start of sync
// Start of Sync is a 1 to 0
	10'd473:
	begin
		LINE <= 10'd474;
		VSYNC <= 1'b0;					// Sync on
	end
// End of sync, start of blanking and porch
	10'd479:
	begin
		LINE <= 10'd480;
		VSYNC <= 1'b1;					// Sync off
	end
// End of porch, start of border
	10'd505:
	begin
		LINE <= 10'd506;
		VBORDER <= 1'b1;
	end
// End of border, start of video, restart state machine
	10'd523:                   // -1
	begin
		LINE <= 10'd000;
		VBLANKING <= 1'b0;
	end
	default:
	begin
		LINE <= LINE + 1'b1;
	end
	endcase
end
