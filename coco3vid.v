			HBORDER <= 1'b1;
			HSYNC <= 1'b1;									// Not H Sync
			if(MODE_256)									// 512 mode
			begin
				HBLANKING <= 1'b1;						// Turn on blanking
				PIXEL_COUNT  <= 10'd592;				// 528 + 64 = 528 + 128 - 64
			end
