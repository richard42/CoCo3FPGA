			({COCO,BP,CRES[0],CHARACTER3[7],CHAR_LATCH_0[13:11]}==7'b0011110)	?	PALETTEE:
			({COCO,BP,CRES[0],CHARACTER3[7],CHAR_LATCH_0[13:11]}==7'b0011111)	?	PALETTEF:
